print("\t\t********Program to reverse a given Number. ex: n=123 Reversed no is 321********\n\n")
num = input("\tEnter The Number To Find Reversed Digit : ")
reverse = ""
strlen = len(num)
for i in range(strlen):
    reverse = reverse + num[strlen - i - 1]
print("\n\tThe Reverse of The Digit Is : ", reverse)



































































