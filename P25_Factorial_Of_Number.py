print("\t\t********Program to find factorial of a given number.********\n\n")
num = int(input("\tEnter The Number To Find Factorial : "))
factorial = 1
for i in range(1,num+1):
    factorial = factorial*i
print("\n\tThe Factorial Of Number Is : ", factorial)



































































