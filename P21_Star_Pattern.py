print("\t\t********Program To check whether a given number is even or odd.********\n\n")
num = int(input("\t\t\t\tEnter The Number Of StarLine : "))

for i in range(num+1):
    for j in range(i):
        print("* ", end = '')
    
    print('')

for i in range(num+1):
    for j in range(num - i - 1):
        print("* ",end = '')
        
    print('')



































































